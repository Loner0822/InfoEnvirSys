using System.Collections.Generic;

namespace PublishSys
{
	public class CompStr : IComparer<string>
	{
		public int Compare(string x, string y)
		{
			int num = int.Parse(x);
			int num2 = int.Parse(y);
			if (num > num2)
			{
				return 1;
			}
			if (num < num2)
			{
				return -1;
			}
			return 0;
		}
	}
}
